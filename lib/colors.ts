import presets from "./gradients.json";

type GradientColor = {
  name: string;
  favorite: boolean;
  index: string;
  deg: number;
  group: string[];
  gradient: { color: string; pos: number }[];
};

export function parseGradient(data: GradientColor) {
  if (
    data &&
    data.gradient &&
    Array.isArray(data.gradient) &&
    data.gradient.length > 0
  ) {
    const deg = data.deg || 0;
    const gradientStops = data.gradient.map(
      (stop) => `${stop.color} ${stop.pos}%`
    );
    return `linear-gradient(${deg}deg, ${gradientStops.join(", ")})`;
  }
  return "";
}

